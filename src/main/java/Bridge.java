public class Bridge {
    private Bridge left;
    private Bridge right;
    private Bridge down;


    public Bridge getLeft() {
        return left;
    }

    public void setLeft(Bridge left) {
        this.left = left;
    }

    public Bridge getRight() {
        return right;
    }

    public void setRight(Bridge right) {
        this.right = right;
    }

    public Bridge getDown() {
        return down;
    }

    public void setDown(Bridge down) {
        this.down = down;
    }
}
